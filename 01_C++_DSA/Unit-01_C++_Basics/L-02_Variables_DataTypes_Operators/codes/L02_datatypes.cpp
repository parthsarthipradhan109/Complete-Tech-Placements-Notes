#include <iostream>
using namespace std;

int main(){
    // Data Types in C++
    int age = 18;
    char grade = 'A'; // Char ek single character store karta hai, isme single quotes ka use hota hai
    float PI = 3.14f; // Float 6-7 digits tak sahi & f likhna jaruri
    bool isSafe = true; // true -> 1, false -> 0
    double Price = 100.99; // Double 15-16 digits tak sahi

    cout << "age:" << age << endl; // endl ka matlab new line
    cout << "grade:" << grade << endl;
    cout << "PI:" << PI << endl;
    cout << "isSafe:" << isSafe << endl;
    cout << "Price:" << Price << endl;

    return 0;
}