#include <iostream>
using namespace std;

int main() {
    // Loop
    // While Loop

    int count = 1; // Initialization
    while (count <= 5) { // Condition
        cout << count << " "; // yahan Cursor abhi bhi same line pe atka hai
        count++; // Updation
    }
    cout << endl; // yahan Cursor next line pe chala jayega

    return 0;
}